import { reactive, watch, computed, ref } from 'vue'

const lang = reactive({
  value: localStorage.getItem('lang') || 'zh'
})

// 用于强制组件重新渲染的计数器
const updateCounter = ref(0)

watch(() => lang.value, (newLang) => {
  localStorage.setItem('lang', newLang)
  // 增加计数器触发所有使用 t() 的 computed 重新计算
  updateCounter.value++
})

function setLang(newLang) {
  if (translations[newLang] || translations[newLang.split('-')[0]]) {
    lang.value = newLang
  }
}

